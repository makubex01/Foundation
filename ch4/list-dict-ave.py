#あるクラスのテスト結果
test_list = [
    {"名前":"田中","国語":80,"算数":45,"社会":90},
    {"名前":"鈴川","国語":62,"算数":70,"社会":58},
    {"名前":"早川","国語":77,"算数":69,"社会":74}
]
#国語の平均点を求める
total = 0
for p in test_list: #繰り返すたびに1つずつ辞書型をpに代入
    total = total + p["国語"] #取り出した辞書型から、「国語」の値を代入
ave = total / len(test_list) #値の合計を、辞書型の数で割る
print(ave)