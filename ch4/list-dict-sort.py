#あるクラスのテスト結果
test_list = [
    {"名前":"田中","国語":80,"算数":45,"社会":90},
    {"名前":"鈴川","国語":62,"算数":70,"社会":58},
    {"名前":"早川","国語":77,"算数":69,"社会":74}
]

#各生徒について合計点を求める
for p in test_list: #繰り返すたびに1つずつ辞書型をpに代入
    p["合計"] = p["国語"] + p["算数"] + p["社会"] #test_listに[合計]を追加

#合計点を生徒を並び替える
#test_listを並び替える
#キー「合計」の値を基準にする
#降順にする
test_list = sorted(test_list,key=lambda v: v["合計"],reverse=True)

#結果を表示
for p in test_list:
    print(p["名前"] + ":" + str(p["合計"])) 